# 修行：反復と分岐の複合
print("--- 連続素振り開始 ---")

for count in range(1, 11):
    # 通常の出力
    print(f"{count}回目！")

    # 5回目だけの特殊イベント
    if count == 5:
        print("💡智慧之王：半分経過。呼吸を整えてください。")

    # 10回目（最後）だけの特殊イベント
    if count == 10:
        print("✨智慧之王：目標回数に到達。素晴らしい集中力です。")

print("--- 修行終了。データ保存を推奨します---")